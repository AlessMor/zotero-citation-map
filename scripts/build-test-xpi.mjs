#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { createRequire } from "node:module";
import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync,
  cpSync,
} from "node:fs";
import { dirname, extname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";
import process from "node:process";

const require = createRequire(import.meta.url);
const scriptDir = dirname(fileURLToPath(import.meta.url));
const rootDir = resolve(scriptDir, "..");
const packagePath = join(rootDir, "package.json");
const pkg = JSON.parse(readFileSync(packagePath, "utf8"));

function loadTypeScript() {
  try {
    return require("typescript");
  } catch {
    const globalRoot = execFileSync("npm", ["root", "-g"], {
      encoding: "utf8",
    }).trim();
    return require(join(globalRoot, "typescript"));
  }
}

const ts = loadTypeScript();
const buildRoot = join(rootDir, ".test-build");
const addonSource = join(rootDir, "addon");
const addonOutput = join(buildRoot, "addon");
const scriptsOutput = join(addonOutput, "content", "scripts");
const bundlePath = join(scriptsOutput, `${pkg.config.addonRef}.js`);
const xpiPath = join(buildRoot, `${pkg.name}-${pkg.version}-test.xpi`);

rmSync(buildRoot, { recursive: true, force: true });
mkdirSync(buildRoot, { recursive: true });
cpSync(addonSource, addonOutput, { recursive: true });
mkdirSync(scriptsOutput, { recursive: true });

function walkFiles(directory) {
  const files = [];
  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    const absolute = join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(absolute));
    } else if (entry.isFile()) {
      files.push(absolute);
    }
  }
  return files;
}

function moduleID(file) {
  return relative(rootDir, file).split(sep).join("/").replace(/\.ts$/, "");
}

const compilerOptions = {
  target: ts.ScriptTarget.ES2022,
  module: ts.ModuleKind.CommonJS,
  moduleResolution: ts.ModuleResolutionKind.Node10,
  esModuleInterop: true,
  allowSyntheticDefaultImports: true,
  resolveJsonModule: true,
  strict: true,
  useUnknownInCatchVariables: true,
  noFallthroughCasesInSwitch: true,
  newLine: ts.NewLineKind.LineFeed,
  removeComments: false,
};

const compiledModules = [];
const sourceFiles = walkFiles(join(rootDir, "src"))
  .filter((file) => extname(file) === ".ts")
  .sort();

for (const file of sourceFiles) {
  let source = readFileSync(file, "utf8");
  // The normal scaffold build injects this compile-time value through esbuild.
  source = source.replace(/\b__env__\b/g, '"production"');
  const result = ts.transpileModule(source, {
    compilerOptions,
    fileName: file,
    reportDiagnostics: true,
  });
  const errors = (result.diagnostics ?? []).filter(
    (diagnostic) => diagnostic.category === ts.DiagnosticCategory.Error,
  );
  if (errors.length > 0) {
    const formatHost = {
      getCanonicalFileName: (name) => name,
      getCurrentDirectory: () => rootDir,
      getNewLine: () => "\n",
    };
    throw new Error(
      ts.formatDiagnosticsWithColorAndContext(errors, formatHost),
    );
  }
  compiledModules.push({ id: moduleID(file), code: result.outputText });
}

const banner = `/*\n * Zotero Citation Map test bundle.\n * Generated by scripts/build-test-xpi.mjs without the production scaffold.\n * Source version: ${pkg.version}\n */\n`;

const moduleDefinitions = compiledModules
  .map(
    ({ id, code }) =>
      `__modules[${JSON.stringify(id)}] = function(require, module, exports) {\n${code}\n};`,
  )
  .join("\n\n");

const bundle = `${banner}(function(global) {
  "use strict";

  const __modules = Object.create(null);
  const __cache = Object.create(null);

  __modules["package.json"] = function(require, module) {
    module.exports = ${JSON.stringify(pkg)};
  };

${moduleDefinitions}

  function __normalize(request, parentID) {
    let candidate;
    if (request.startsWith(".")) {
      const parentParts = parentID.split("/");
      parentParts.pop();
      const requestParts = request.split("/");
      const parts = parentParts;
      for (const part of requestParts) {
        if (!part || part === ".") continue;
        if (part === "..") parts.pop();
        else parts.push(part);
      }
      candidate = parts.join("/");
    } else {
      candidate = request;
    }

    candidate = candidate.replace(/\\\\/g, "/");
    const candidates = [
      candidate,
      candidate.replace(/\\.(?:js|ts)$/, ""),
      candidate.endsWith(".json") ? candidate : candidate + ".json",
      candidate.replace(/\\/$/, "") + "/index",
    ];
    for (const id of candidates) {
      if (Object.prototype.hasOwnProperty.call(__modules, id)) return id;
    }
    throw new Error(
      "Citation Map test bundle could not resolve module " +
        JSON.stringify(request) +
        " from " +
        JSON.stringify(parentID),
    );
  }

  function __load(request, parentID) {
    const id = __normalize(request, parentID || "");
    if (__cache[id]) return __cache[id].exports;
    const module = { exports: {} };
    __cache[id] = module;
    const localRequire = (child) => __load(child, id);
    __modules[id](localRequire, module, module.exports);
    return module.exports;
  }

  __load("src/index", "");
})(this);\n`;

writeFileSync(bundlePath, bundle, "utf8");

const replacements = new Map([
  ["__addonName__", pkg.config.addonName],
  ["__buildVersion__", pkg.version],
  ["__description__", pkg.description],
  ["__homepage__", pkg.homepage],
  ["__author__", pkg.author],
  ["__addonID__", pkg.config.addonID],
  ["__addonRef__", pkg.config.addonRef],
  ["__addonInstance__", pkg.config.addonInstance],
  [
    "__updateURL__",
    `https://github.com/AlessMor/zotero-citation-map/releases/download/release/update.json`,
  ],
]);

const textExtensions = new Set([
  ".js",
  ".json",
  ".xhtml",
  ".css",
  ".ftl",
  ".svg",
  ".txt",
  ".md",
]);
for (const file of walkFiles(addonOutput)) {
  if (!textExtensions.has(extname(file))) continue;
  let text = readFileSync(file, "utf8");
  for (const [needle, value] of replacements) {
    text = text.split(needle).join(String(value));
  }
  writeFileSync(file, text, "utf8");
}

execFileSync(process.execPath, ["--check", bundlePath], { stdio: "inherit" });
if (existsSync(xpiPath)) rmSync(xpiPath);
execFileSync("zip", ["-q", "-r", xpiPath, "."], {
  cwd: addonOutput,
  stdio: "inherit",
});

process.stdout.write(`Test bundle: ${bundlePath}\n`);
process.stdout.write(`Test XPI: ${xpiPath}\n`);
